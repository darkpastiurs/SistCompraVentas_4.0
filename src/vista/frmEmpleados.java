    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        boolean validarTextos = (validar.validarCamposTexto(txtNombre) && validar.validarCamposTexto(txtApellidos)
                && validar.validarCamposTexto(ftxtDUI) && validar.validarCamposTexto(txtUsuario)
                && validar.validarCamposTexto(ftxtNIT)
                && validar.validarCamposTexto(ftxtTelefono)
                && validar.validarCamposTexto(txtDireccion));
        boolean validarFecha = validar.validarFechas(dcFechaNacimiento);
        boolean validarContrasenas = (Arrays.equals(txtContrasena.getPassword(), txtContrasena1.getPassword()));
        if (validarTextos && validarFecha) {
            Empleado empleado = new Empleado();
            Usuario usuario = new Usuario();
            Nivel nivel_o = (Nivel) cboNivel.getSelectedItem();
            empleado.setNombre(txtNombre.getText().trim());
            String[] partirApellidos = validar.editarApellidos(txtApellidos.getText().trim());
            if(partirApellidos != null){
                if (partirApellidos.length > 1) {
                    empleado.setApellidoPaterno(partirApellidos[0]);
                    empleado.setApellidoMaterno(partirApellidos[1]);
                }
            } else {
                empleado.setApellidoPaterno(txtApellidos.getText().trim());
            }
            empleado.setDUI(ftxtDUI.getText());
            empleado.setNIT(ftxtNIT.getText());
            empleado.setFechaNacimiento(dcFechaNacimiento.getDate());
            empleado.setGenero(String.valueOf(cboGenero.getSelectedItem().toString().charAt(0)));
            empleado.setDireccion(txtDireccion.getText().trim());
            empleado.setTelefono(ftxtTelefono.getText());
            if (!txtEmail.getText().trim().equals("")) {
                if (validar.validarEmail(txtEmail.getText().trim(), txtEmail)) {
                    empleado.setEmail(txtEmail.getText().trim());
                }
            }
            usuario.setNickname(txtUsuario.getText().trim());
            usuario.setEstado(chkEstado.isSelected());
            if (editar == false) { // va a ingresar nuevo
                if (validar.validarCamposTexto(txtContrasena)) {
                    if (validarContrasenas) {                        
                        usuario.setContrasena(String.valueOf(txtContrasena.getPassword()));
                        if (controlador.Registrar(empleado, usuario, nivel_o)) {
                            JOptionPane.showMessageDialog(this,
                                    "El registro ha sido ingresado exitosamente",
                                    "Sistema de Compras y Ventas - Empleados",
                                    JOptionPane.INFORMATION_MESSAGE);  
                            finalizarIngreso();
                        }
                    } else {
                        JOptionPane.showMessageDialog(this,
                                "Las contrasenas no concuerdan vuelve a intentarlo",
                                "Sistema de Compras y Ventas - Validaciones",
                                JOptionPane.WARNING_MESSAGE);
                        txtContrasena.setText("");
                        txtContrasena1.setText("");
                    }
                } else {
                   JOptionPane.showMessageDialog(this,
                        "Las debes ingresar la contrase単a",
                        "Sistema de Compras y Ventas - Validaciones",
                        JOptionPane.WARNING_MESSAGE);
                }
            } else { // si se va a editar existente
                empleado.setId(idEmpleado);
                if(txtContrasena.getPassword().length > 0){
                    usuario.setContrasena(String.valueOf(txtContrasena.getPassword()));
                }
                int respuesta = JOptionPane.showConfirmDialog(this, "多Estas seguro de editar estos datos?", "Sistema de Compra y Venta - Empleados",
                    JOptionPane.YES_NO_OPTION);
                if(respuesta == JOptionPane.YES_OPTION){                   
                    if(controlador.Editar(empleado, usuario, nivel_o)){
                        JOptionPane.showMessageDialog(this,
                                "El registro ha sido actualizado exitosamente",
                                "Sistema de Compras y Ventas - Empleados",
                                JOptionPane.INFORMATION_MESSAGE);
                        finalizarIngreso();
                    } 
                }
            }           
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int resp = JOptionPane.showConfirmDialog(this, "多Estas seguro de eliminar a este empleado?", 
                "Sistema de Compra y Venta - Empleado", JOptionPane.YES_NO_OPTION);
        if(resp == JOptionPane.YES_OPTION){            
            if(controlador.Borrar(new Empleado(idEmpleado))){
                JOptionPane.showMessageDialog(this, "El empleado ha sido eliminado con exito", 
                        "Sistema de Compra y Venta - Empleado", JOptionPane.INFORMATION_MESSAGE);
                finalizarIngreso();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

