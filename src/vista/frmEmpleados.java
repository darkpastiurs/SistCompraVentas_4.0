    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        frmBuscarEmpleados frmBuscar = new frmBuscarEmpleados(this, true);
        frmBuscar.setVisible(true);
        if(frmBuscar.isVisible() == false){
            if(frmBuscar.usuarioActual.getNickname() != null){
                txtNombre.setText(frmBuscar.usuarioActual.getEmpleado().getNombre());
                txtApellidos.setText((frmBuscar.usuarioActual.getEmpleado().getApellidoPaterno() + " "
                        + frmBuscar.usuarioActual.getEmpleado().getApellidoMaterno()).trim());
                ftxtDUI.setText(frmBuscar.usuarioActual.getEmpleado().getDUI());
                ftxtNIT.setText(frmBuscar.usuarioActual.getEmpleado().getNIT());
                validar.SelectedItem(cboGenero, frmBuscar.usuarioActual.getEmpleado().getGenero());
                dcFechaNacimiento.setDate(new Date(frmBuscar.usuarioActual.getEmpleado().getFechaNacimiento().getTime()));
                txtUsuario.setText(frmBuscar.usuarioActual.getNickname());
                chkEstado.setSelected(frmBuscar.usuarioActual.isEstado());
                validar.SelectedItem(cboNivel, frmBuscar.usuarioActual.getNivel());
                ftxtTelefono.setText(frmBuscar.usuarioActual.getEmpleado().getTelefono());
                txtEmail.setText(frmBuscar.usuarioActual.getEmpleado().getEmail());
                txtDireccion.setText(frmBuscar.usuarioActual.getEmpleado().getDireccion());
                idEmpleado = frmBuscar.usuarioActual.getEmpleado().getId();
                validar.estadosBotones(3, arrayBotones); 
                frmBuscar.dispose();
            }            
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

