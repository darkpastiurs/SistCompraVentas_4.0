    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        boolean validarTexto = validar.validarCamposTexto(txtNombre) && validar.validarCamposTexto(ftxtNIT)
                && validar.validarCamposTexto(txtRegistro) && validar.validarCamposTexto(txtaDireccion)
                && validar.validarCamposTexto(ftxtTelefono);
        if (validarTexto) {
            Proveedor proveedor = new Proveedor();
            proveedor.setNombre(txtNombre.getText().trim());
            proveedor.setNIT(ftxtNIT.getText());
            proveedor.setNRC(txtRegistro.getText());
            proveedor.setDireccion(txtaDireccion.getText());
            proveedor.setTelefono(ftxtTelefono.getText());
            if (!txtEmail.getText().trim().equals("")) {
                if (validar.validarEmail(txtEmail.getText().trim(), txtEmail)) {
                    proveedor.setEmail(txtEmail.getText());
                }
            }
            if (!txtSitioWeb.getText().trim().equals("")) {
                if (validar.validarURL(txtSitioWeb.getText().trim(), txtSitioWeb)) {
                    proveedor.setWebsite(txtSitioWeb.getText().trim());
                }
            }
            if (editar == false) {
                if (controlador.Registrar(proveedor)) {
                    JOptionPane.showMessageDialog(this,
                            "El proveedor se ha registrdo exitosamente", "Sistema de Compras y Ventas - Proveedores",
                            JOptionPane.INFORMATION_MESSAGE);
                    finalizarIngreso();
                }
            } else {
                proveedor.setId(idProveedor);
                int respuesta = JOptionPane.showConfirmDialog(this, "¬øEstas seguro de editar estos datos?", "Sistema de Compra y Venta - Proveedores",
                        JOptionPane.YES_NO_OPTION);
                if (respuesta == JOptionPane.YES_OPTION) {
                    if (controlador.Editar(proveedor)) {
                        JOptionPane.showMessageDialog(this,
                                "El proveedor se ha editado exitosamente", "Sistema de Compras y Ventas - Proveedores",
                                JOptionPane.INFORMATION_MESSAGE);
                    }
                    finalizarIngreso();
                }

            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

